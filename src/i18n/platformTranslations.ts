// Helper function to get platform description in current language
export const getPlatformDescription = (
  platformId: string, 
  language: SupportedLanguage
): string => {
  const platform = platformTranslations[platformId];
  if (!platform) return ''; // Return empty if platform not found
  
  // Try requested language, fallback to English
  return platform[language]?.description || platform.en?.description || '';
};

